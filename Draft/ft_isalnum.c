#include "libft.h"
int	ft_isalnum(int i)
{
	if ((i >= 65 && i <= 90) || (i >= 97 && i <= 122))
	{
		return (0);
	}
	else if (i >= 48 && i <= 57)
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
