#include "libft.h"
#include <stdio.h>

int	ft_isdigit(int i)
{
	if ((i >= 48) && (i <= 57))
	{
		return (0);
	}
	else
		return (1);
}

int	main(void)
{
	printf("%d\n", ft_isdigit('s'));
	return (0);
}
