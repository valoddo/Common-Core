#include "libft.h"
#include <stdio.h>

int	ft_toupper(int c)
{
	while(c >= 'a' && c <= 'z')
	{
		c -= 32;
	}
	return(c);
}

int	main(void)
{
	printf("%d\n", ft_toupper('c'));
	return (0);
}
