#include "ft_printf.h"

int	ft_print_int(int di)
{
	int	i;
	int	j;

	if (di == -2147483648)
		return (write(1, "-2147483648", 11), 11);
	j = 0;
	if (di < 0)
	{
		write(1, "-", 1);
		di = di * -1;
		j++;
	}
	i = 1;
	while (di / i > 9)
		i *= 10;
	while (i > 0)
	{
		write(1, &"0123456789"[di / i], 1);
		di %= i;
		i /= 10;
		j++;
	}
	return (j);
}
