#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

#include <stdio.h>
int main()
{
        char *str = "Hello";
        int i = ft_strlen(str);
        printf("%d", i);
        return (0);
}
