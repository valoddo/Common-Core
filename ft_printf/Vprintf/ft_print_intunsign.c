#include "ft_printf.h"

int	ft_print_intunsign(unsigned int u)
{
	int	i;
	int	j;

	i = 1;
	while (u / i > 9)
		i *= 10;
	j = 0;
	while (i > 0)
	{
		write(1, &"0123456789"[u / i], 1);
		u %= i;
		i /= 10;
		j++;
	}
	return (j);
}
