#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;
	int		i;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	i = 1;
	while (n / i > 9)
	{
		i *= 10;
	}
	while (i > 0)
	{
		c = (n / i) + '0';
		n %= i;
		write(fd, &c, 1);
		i /= 10;
	}
}
